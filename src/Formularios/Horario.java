    public void settingtime(){
        try {
            Calendar gc = new GregorianCalendar();
            int h1 = gc.get(gc.HOUR_OF_DAY);
            int m1 = gc.get(gc.MINUTE);
            jComboBox1.setSelectedIndex(h1);
            jComboBox2.setSelectedIndex(m1);
            
            //hora cierre
            if (m1 == 59){
                SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
                Date d2 = df.parse("2016-05-30 " + (h1 + 1) + ":" +  "00:00");
                Calendar gc2 = new GregorianCalendar();
                gc2.setTime(d2);
                if (h1 > 23){
                    Date d3 = df.parse("2016-05-30 00:00:00");
                    gc2.setTime(d3);
                }
                int h2 = gc2.get(gc2.HOUR_OF_DAY);
                int m2 = gc2.get(gc2.MINUTE);
                jComboBox3.setSelectedIndex(h2);
                jComboBox4.setSelectedIndex(m2);
            } else {
                SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
                Date d2 = df.parse("2016-05-30 " + h1 + ":" + (m1 + 1) + ":00");
                Calendar gc2 = new GregorianCalendar();
                gc2.setTime(d2);
                int h2 = gc2.get(gc2.HOUR_OF_DAY);
                int m2 = gc2.get(gc2.MINUTE);
                jComboBox3.setSelectedIndex(h2);
                jComboBox4.setSelectedIndex(m2);
            }
        } catch (ParseException ex) {
            Logger.getLogger(Horario.class.getName()).log(Level.SEVERE, null, ex);
        }
            
    }
    
