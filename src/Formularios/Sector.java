    public void LlenarDatosEmpleado(){
        //Conectando la clase Conexion con la clase actual
        Conexion con = new Conexion();
        cn = con.conectar();
        try{
            //escribiendo la consulta 
            String sql = "SELECT * FROM Empleado where cod_cargo_empleado = 3";
            //enviando la consulta al cmd
            PreparedStatement cmd = cn.prepareStatement(sql);
            ResultSet rs = cmd.executeQuery();
            //mostrando los valores en el combo box
            while(rs.next()){
                jComboBox1.addItem(rs.getString("nombre_empleado"));
            }
        } catch (Exception ex){
            System.out.println("ERROR: " + ex.getMessage());
        }
    }
    
