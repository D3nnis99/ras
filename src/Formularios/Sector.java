    private void jLabel40MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel40MouseClicked
        if (jTextField2.getText().equals("") || (jComboBox1.getSelectedIndex() == - 1) || (jComboBox2.getSelectedIndex() == - 1) || (jComboBox3.getSelectedIndex() == - 1)){
            JOptionPane.showMessageDialog(this, "Campos vacios");
        } else {
            //declarando variable de la clase que contiene los metodos
            Mto_Sector obj = new Mto_Sector();
            //insertando los nuevos valores a los metodos de la clase
            obj.setSector(jTextField2.getText());
            obj.setEmpleado((jComboBox1.getSelectedIndex() + 1));
            obj.setAgrupacion((jComboBox2.getSelectedIndex() + 1));
            obj.setHorario((jComboBox3.getSelectedIndex() + 1));
            //ejecuta el metodo y valida si se realizo satisfactoriamente
            if(obj.MthGuardar()){
                JOptionPane.showMessageDialog(this, "Datos guardados");
            } else {
                JOptionPane.showMessageDialog(this, "Error al guardar los datos");
            }
            //ejecuta el metodo de limpiar campos
            mthLimpiarCampos();
            mthConsultar();
        }
    }//GEN-LAST:event_jLabel40MouseClicked

    private void jLabel44MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel44MouseClicked
        if (jTextField2.getText().equals("") || cod_sector == null || (jComboBox1.getSelectedIndex() == - 1) || (jComboBox2.getSelectedIndex() == - 1) || (jComboBox3.getSelectedIndex() == - 1)){
            JOptionPane.showMessageDialog(this, "Campos vacios o registro no seleccionado.");
        } else {
            //declarando variable de la clase que contiene los metodos
            Mto_Sector obj = new Mto_Sector();
            //insertando los nuevos valores ingresados por los usuarios a los metodos de la clase
            obj.setSector(jTextField2.getText());
            obj.setEmpleado((jComboBox1.getSelectedIndex() + 1));
            obj.setAgrupacion((jComboBox2.getSelectedIndex() + 1));
            obj.setHorario((jComboBox3.getSelectedIndex() + 1));
            obj.setCodigo(Integer.parseInt(cod_sector));
            //ejecuta el metodo y valida si se realizo satisfactoriamente
            if (obj.mthModificar()){
                JOptionPane.showMessageDialog(this, "Datos modificados");
            } else {
                JOptionPane.showMessageDialog(this, "Error al modificar");
            }
            //ejecuta el metodo de limpiar campos
            mthLimpiarCampos();
            mthConsultar();
        }
    }//GEN-LAST:event_jLabel44MouseClicked

    private void jLabel45MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel45MouseClicked
        if (cod_sector == null){
            JOptionPane.showMessageDialog(this, "No ha seleccionado un registro para borrar.");
        } else {
            //declarando variable de la clase que contiene los metodos
            Mto_Sector obj = new Mto_Sector();
            //insertando el codigo al metodo setCodigo() en la clase
            obj.setCodigo(Integer.parseInt(cod_sector));
            //muestra un mensaje al usuario de confirmacion
            int eliminar = JOptionPane.showConfirmDialog(this, "¬øSeguro?");
            //valida la respuesta del usuario
            if (eliminar == 0) {
                if (obj.mthEliminar()){
                    JOptionPane.showMessageDialog(this, "Datos eliminados");
                } else {
                    JOptionPane.showMessageDialog(this, "Error al eliminar");
                }
            }
            //ejecuta el metodo de limpiar campos
            mthLimpiarCampos();
            mthConsultar();
        }
    }//GEN-LAST:event_jLabel45MouseClicked

